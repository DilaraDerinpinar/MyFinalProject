using Business.Abstract;
using Business.Concrete;
using DataAccess.Abstract;
using DataAccess.Concrete.EntityFramework;
using Microsoft.AspNetCore.Builder;
using Microsoft.AspNetCore.Hosting;
using Microsoft.AspNetCore.HttpsPolicy;
using Microsoft.AspNetCore.Mvc;
using Microsoft.Extensions.Configuration;
using Microsoft.Extensions.DependencyInjection;
using Microsoft.Extensions.Hosting;
using Microsoft.Extensions.Logging;
using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace WebAPI
{
    public class Startup
    {
        public Startup(IConfiguration configuration)
        {
            Configuration = configuration;
        }

        public IConfiguration Configuration { get; }

        // This method gets called by the runtime. Use this method to add services to the container.
        public void ConfigureServices(IServiceCollection services)
        {
            //AutoFac,Ninject... gibi IoC container alt yapýsý hizmeti verenler de vardýr.
            //AOP = Bir metodun önünde veya sonunda hata verdiðinde çalýþan kod parçacýklarýný bu mimari ile yazarýz. Business katmanýnda yazýlabilir. [] parantezler içinde yazýlýr. bu sebeple autofac gibi hizmetler kullanýlýr. asp.net in kendi IoC alt yapýsý bu kadarýna yeterli deðildir.
            services.AddControllers();
            //singleton : uygulama ilk ayaða kalktýðý anda, servisin tek bir instance’ý oluþturularak memory’de 
            //tutulur ve daha sonrasýnda her servis çaðrýsýnda bu instance gönderilir. Ancak içerisinde parametre olmamasý lazým.
            services.AddSingleton<IProductService, ProductManager>();
            services.AddSingleton<IProductDal, EfProductDal>();
        }

        // This method gets called by the runtime. Use this method to configure the HTTP request pipeline.
        public void Configure(IApplicationBuilder app, IWebHostEnvironment env)
        {
            if (env.IsDevelopment())
            {
                app.UseDeveloperExceptionPage();
            }

            app.UseHttpsRedirection();

            app.UseRouting();

            app.UseAuthorization();

            app.UseEndpoints(endpoints =>
            {
                endpoints.MapControllers();
            });
        }
    }
}
